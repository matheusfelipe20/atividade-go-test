package services

//Função de Soma
func Sum(a int, b int) int {
	return a + b
}

//Função de Subtração
func Subtraction(a int, b int) int {
	return a - b
}

//Função de Divisão
func Division(a int, b int) int {
	return a / b
}

//Função de Multiplicação
func Multiplication(a int, b int) int {
	return a * b
}